    // Calculates the interpolated height at the given coordinates
    public sampleHeight(x: number, z: number): number {
        let L = 21, origin = -50, spacing = 1.0/5.0;
        // Get coordinates in the plane's coordinate system
        let xo = Math.max(0, Math.min(spacing * (x - origin), L - 2));
        let zo = Math.max(0, Math.min(spacing * (z - origin), L - 2));
        let xl = Math.floor(xo), zl = Math.floor(zo);
        // Get the height at the four nearest neighbours
        let sx = 8, sz = 8 * L;
        let index = 1 + xl * sx + zl * sz;
        let xlzl = ArenaActor.vertexData[index];
        let xlzu = ArenaActor.vertexData[index + sz];
        let xuzl = ArenaActor.vertexData[index + sx];
        let xuzu = ArenaActor.vertexData[index + sx + sz];
        // Calculate weights for each neighbour
        let dx = xo - xl, dz = zo - zl;
        let idx = 1.0 - dx, idz = 1.0 - dz;
        // Calculate the interpolated height
        return xlzl*idx*idz + xlzu*idx*dz + xuzl*dx*idz + xuzu*dx*dz;
    }

